function init(){
    myMap = new ymaps.Map ("map", {
        center: [60.76, 97.64],
        zoom: 3
    });

    myMap.controls.add(
        new ymaps.control.ZoomControl()
    );

    $.each(cities, function(i, item){
        var myGeocoder = ymaps.geocode(item);
        myGeocoder.then(
            function (res) {
                var myCoords = res.geoObjects.get(0).geometry.getCoordinates();
                myMap.geoObjects.add(new ymaps.Placemark(myCoords, {
                    balloonContent: item
                }));
            },
            function (err) {
            }
        );

    });

}