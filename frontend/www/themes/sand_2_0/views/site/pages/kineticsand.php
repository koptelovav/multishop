<h3 id="production">Все о кинетическом песке</h3>
<p>
    Кинетический песок, уже больше двух лет, радует детей и их родителей своими необычными свойствами. За это время у
    покупателей сформировался целый ряд вопросов об этом материале, его свойствах, качестве и разновидности. Сегодня мы
    попытаемся рассказать вам как можно больше о кинетическом песке, опираясь на наш опыт и опыт коллег из других стран,
    где эта игрушка продается уже много лет.
</p>
<h3 id="production">Производство</h3>
<p>
    Кинетический песок производится только в Швеции, все остальные виды похожего материала, это подделки и называться
    кинетическим песком не могут. Процесс производства как прост, так и сложен. Песок промывается, сушится, а после
    сортируется по фракции. Кинетическим обычный песок становится после смешивания его с особым связующим, которое, по
    сути, является обычным силиконом. Этот процесс смешивания и дальнейшей обработки держится компанией в секрете и
    повторить данную технологию пока никому не удалось. Аналоги, представленные сейчас на Российском рынке, представляют
    собой песок, перемешанный с силиконовым маслом и связующее при такой технике покрытия песка не закреплено на нем,
    поэтому остается на руках и поверхностях. В кинетическом песке, связующий компонент нельзя удалить с песчинок,
    поэтому он (компонент) не остается на руках и поверхностях.
</p>

