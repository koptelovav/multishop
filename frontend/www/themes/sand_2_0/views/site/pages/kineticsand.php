<h3>Свойства кинетического песка</h3>
<p>
    Оригинальный кинетический песок должен обладать набором следующих свойств:<br />
    - При формировании его в небольшую формочку, держит форму, при этом готовую формочку можно разрезать ножом и ее
    части не распадутся.<br />
    - При надавливании на готовую постройку, песок должен расползаться под руками (не крошится)<br />
    - На руках после игры с кинетическим песком не должно оставаться связующего агента (никаких масленых пятен, жидкости
    или кусочков силикона)<br />
    - Кинетический песок не должен прилипать к пластику, металлу или стеклу, однако он прилипает к силиконовым формочкам
    и коврикам.<br />
    - На ощупь кинетический песок ощущается как мокрый песок<br />
    - При растягивание небольшой кучки песка, песчинки цепляются друг за друга, но не приклеиваются.<br />
    - Кинетический песок хорошо держит низкие формочки, однако столбики высотой более 8 см, начинают расползаться под
    собственным весом. Кинетический песок, называется так потому что он двигается, от него не стоит ожидать свойств
    пластилина.<br />
    - Песчинки кинетического песка могут прилипать к рукам, особенно к детским, так как они зачастую бывают влажными.
    После игры руки можно отряхнуть. Песок является сыпучим материалом, и это нормально.<br />
    - Кинетический песок после вскрытия упаковки может иметь силиконовый запах, ему необходимо дать выветрится перед
    первым применением. В дальнейшем материал имеет легкий запах и улавливается некоторыми нашими покупателями, это
    зависит от индивидуальной чувствительности.<br />
    - В зависимости от условий окружающей среды, кинетический песок незначительно изменяет свои свойства, подробнее
    читайте в разделе «уход и хранение»<br />
    - При повышении температуры, кинетический песок становится более текучим и песчинки песка прилипают к рукам сильнее.
    При охлаждении песок становится тугой и лучше держит форму.<br />
    - Цветной кинетический песок не пачкает рук краской. Краска изготовлена на силиконовой основе и не смывается с
    песчинок при его промывке<br />
    - Цветной кинетический песок смешивается между собой и его нельзя разделить.<br />
</p>

<h3>Уход и хранение</h3>
<p>
    Так как песок изготовлен из натурального материала, идеальным условием хранения для кинетического песка, является
    умеренная влажность 50-60%, прохладное место с открытой крышкой. Однако от неправильного хранения песок не портится,
    он меняет свои свойства, которые в дальнейшем могут быть восстановлены.
    Если песок долгое время хранится герметично, то после вскрытия, короткое время, может улавливаться силиконовый
    запах.
<p>
</p>
    Если кинетический песок намочить, то необходимо просушить его выложив тонким слоем до восстановления его свойств.
    Если кинетический песок стал более текучий и липкий, значит имеет место повышенная влажность, поры песка набрали в
    себя влагу. Обычно такая ситуация случается в летний период или в сезон дождей, а также при работающем увлажнителе.
    В этом случае он станет суше, когда поменяются условия окружающей среды, либо можно просушить его искусственно.
    Если в помещении сухо и прохладно, то силикон сжимается, песок становится более тугим, тяжелее трамбуется в
    формочку, однако держит форму лучше и дольше.
<p>
</p>
    Если в песок попал посторонний мусор, то убирать его необходимо вручную.
    Если песок испачкался жидкостью, например, в него что-то пролили, то его необходимо промыть и полностью высушить,
    связующий агент не смывается с песчинок.
<p>
</p>
    Если хранить цветной кинетический песок на солнце, то верхний его слой может потерять цвет. В этом случае просто
    перемешайте массу, и разница между песчинками исчезнет.
    Оригинальность товара и надежность продавца
<p>
</p>
